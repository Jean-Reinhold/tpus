#!/usr/bin/env node
/**
 * Scaffold a new deliverable from a template.
 *
 * Usage:
 *   node scripts/new-deliverable.mjs <doc|ppt> <kebab-case-name> [--date YYYY-MM-DD]
 *
 * Creates deliverable-assets/{documents|presentations}/<date>-<name>/ at the
 * repo root with:
 *   index.html   — the template, asset paths rewritten to the local assets/
 *   assets/      — fonts, marcas, chart vendor + theme (self-contained:
 *                  the folder can be zipped and shared as-is)
 *   RULES.md     — copy of the template's layout rules for quick reference
 *
 * Then edit index.html and render with:
 *   node scripts/render.mjs <deliverable-dir> --debug [--pdf]
 */
import { cp, mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { execSync } from 'node:child_process';

const SKILL_DIR = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');

const [type, name, ...rest] = process.argv.slice(2);
if (!['doc', 'ppt'].includes(type) || !name || !/^[a-z0-9][a-z0-9-]*$/.test(name)) {
  console.error('Usage: node scripts/new-deliverable.mjs <doc|ppt> <kebab-case-name> [--date YYYY-MM-DD]');
  process.exit(1);
}
const dateFlag = rest.indexOf('--date');
const date =
  dateFlag >= 0
    ? rest[dateFlag + 1]
    : new Date().toISOString().slice(0, 10);
if (!/^\d{4}-\d{2}-\d{2}$/.test(date)) {
  console.error(`Bad --date value: ${date} (expected YYYY-MM-DD)`);
  process.exit(1);
}

const repoRoot = execSync('git rev-parse --show-toplevel', { encoding: 'utf8' }).trim();
const bucket = type === 'doc' ? 'documents' : 'presentations';
const destDir = path.join(repoRoot, 'deliverable-assets', bucket, `${date}-${name}`);
if (existsSync(destDir)) {
  console.error(`Already exists: ${destDir}`);
  process.exit(1);
}

await mkdir(path.join(destDir, 'assets'), { recursive: true });
await mkdir(path.join(destDir, 'pages'), { recursive: true });

// template fragments + rules — one pages/NN-*.html per page/slide;
// index.html is generated by render.mjs, never authored
const tplDir = path.join(SKILL_DIR, 'templates', type);
const rewrite = (s) => s.replaceAll('../../static/', 'assets/');
for (const f of ['head.html', 'styles.css']) {
  await writeFile(path.join(destDir, f), rewrite(await readFile(path.join(tplDir, f), 'utf8')));
}
for (const f of (await readdir(path.join(tplDir, 'pages'))).filter((f) => f.endsWith('.html'))) {
  await writeFile(
    path.join(destDir, 'pages', f),
    rewrite(await readFile(path.join(tplDir, 'pages', f), 'utf8')),
  );
}
await cp(path.join(tplDir, 'RULES.md'), path.join(destDir, 'RULES.md'));

// self-contained assets: fonts, marcas, chart runtime
await cp(path.join(SKILL_DIR, 'static', 'fonts'), path.join(destDir, 'assets', 'fonts'), { recursive: true });
await cp(path.join(SKILL_DIR, 'static', 'vendor'), path.join(destDir, 'assets', 'vendor'), { recursive: true });
for (const f of ['ufpel-charts.js', 'ufpel-marca-white.svg', 'ufpel-marca.svg', 'ufpel-marca.png', 'ufpel-escudo.png', 'ufpel-hero-bg.jpg']) {
  await cp(path.join(SKILL_DIR, 'static', f), path.join(destDir, 'assets', f));
}

console.log(
  JSON.stringify(
    {
      ok: true,
      dir: destDir,
      next: [
        `edit ${path.join(destDir, 'pages')}/*.html (one file = one page; index.html is generated)`,
        `node ${path.join(SKILL_DIR, 'scripts', 'render.mjs')} ${destDir} --debug`,
      ],
    },
    null,
    2,
  ),
);
